var language = {
    langValue: localStorage.getItem("langValue") || "cn",
    translate: function (lang) {
        localStorage.setItem("langValue", lang);
        window.location.href = location.href + '?time=' + new Date().getTime();
    },
    getLangValue: function (key, defaultValue) {
        if (this.langValue == "cn") {
            return defaultValue;
        }
        if (window.location.href.indexOf("coolkit")== -1) {
            return defaultValue;
        }
        var findKey = function (item) {
            return item.resourceId == key
        };
        var item = langData.find(findKey);
        return (item != undefined ? item.english : defaultValue);
    },
    init: function () {
        // 获取自定义属性标签
        //console.log($("[lang-key]"));
        if (this.langValue != "cn") {
            $("[lang-key]").each(function () {
                var langValue = language.getLangValue($(this).attr("lang-key"), "");
                if (langValue != "") {
                    var nodeName = this.nodeName.toLowerCase();
                    if (nodeName == "title") {
                        $(document).attr("title", langValue);
                    } else if (nodeName == "input" && this.type == "text") {
                        $(this).attr('placeholder', langValue)
                    } else if (nodeName == "input" && this.type == "button") {
                        $(this).attr("value", langValue);
                    } else {
                        $(this).html(langValue);
                    }
                }
            });
        }
    }
};

